"""
Generate publication-quality figures for IEEE Access resubmission from
results_raw.csv and results_summary.csv.

Outputs (in ./figures/):
    1. time_accuracy_all.pdf     -- wall-clock (log) vs accuracy, faceted 2x3
    2. box_plots_all.pdf         -- accuracy distributions per method, faceted 2x3
    3. ilpd_svm_story.pdf        -- hero figure: Manual degeneracy vs HPO recovery
    4. summary_scatter.pdf       -- single-panel overview across all 12 cells
    5. meta_heuristic_analysis.pdf -- 4-panel figure (two real, two placeholder)
       panels (c) box plot PD/RF and (d) time vs accuracy PD/RF are real;
       panels (a) convergence and (b) diversity are placeholders to be
       regenerated by the supplementary Colab notebook.
"""

from __future__ import annotations

import os
from pathlib import Path

import matplotlib.pyplot as plt
import matplotlib as mpl
import numpy as np
import pandas as pd

# ----------------------------------------------------------------------
# Matplotlib style (camera-ready, black-and-white-friendly)
# ----------------------------------------------------------------------
mpl.rcParams.update({
    "font.family": "serif",
    "font.serif":  ["DejaVu Serif", "Times New Roman", "Times"],
    "font.size":   9.5,
    "axes.titlesize":  10,
    "axes.labelsize":  9.5,
    "legend.fontsize": 8.5,
    "xtick.labelsize": 8.5,
    "ytick.labelsize": 8.5,
    "axes.spines.top":   False,
    "axes.spines.right": False,
    "figure.dpi": 120,
    "pdf.fonttype": 42,   # editable in Illustrator
    "ps.fonttype":  42,
})

ROOT = Path(__file__).parent
FIG  = ROOT / "figures"
FIG.mkdir(exist_ok=True)

METHOD_ORDER  = ["Manual", "Grid", "Random", "Bayesian", "GWO", "PSO"]
METHOD_COLORS = {
    "Manual":   "#555555",
    "Grid":     "#D95F02",
    "Random":   "#1B9E77",
    "Bayesian": "#7570B3",
    "GWO":      "#E7298A",
    "PSO":      "#66A61E",
}
METHOD_MARKERS = {
    "Manual":   "X",
    "Grid":     "s",
    "Random":   "o",
    "Bayesian": "D",
    "GWO":      "^",
    "PSO":      "v",
}

DATASET_LABEL = {
    "BC":   "Breast Cancer (n=569)",
    "HD":   "Heart Disease (n=270)",
    "PD":   "Pima Diabetes (n=768)",
    "ILPD": "ILPD Liver (n=583)",
}
CLF_LABEL = {"RF": "Random Forest", "SVM": "SVM (RBF)", "LR": "Logistic Regression"}


def load_data():
    raw = pd.read_csv(ROOT / "results_raw.csv")
    summ = pd.read_csv(ROOT / "results_summary.csv")
    raw["method"]  = pd.Categorical(raw["method"],  categories=METHOD_ORDER, ordered=True)
    summ["method"] = pd.Categorical(summ["method"], categories=METHOD_ORDER, ordered=True)
    return raw, summ


# ----------------------------------------------------------------------
# Figure 1: time vs accuracy, 2x3 facet (2 datasets x 3 classifiers)
# ----------------------------------------------------------------------
def fig_time_accuracy_facet(raw: pd.DataFrame, summ: pd.DataFrame):
    datasets    = ["BC", "HD", "PD", "ILPD"]
    classifiers = ["RF", "SVM", "LR"]

    fig, axes = plt.subplots(4, 3, figsize=(9.6, 10.4), sharex=False, sharey=False)

    for r, ds in enumerate(datasets):
        for c, clf in enumerate(classifiers):
            ax = axes[r, c]
            sub = summ[(summ["dataset"] == ds) & (summ["classifier"] == clf)]

            for m in METHOD_ORDER:
                row = sub[sub["method"] == m]
                if row.empty:
                    continue
                t = float(row["time_mean"].iloc[0])
                a = float(row["acc_mean"].iloc[0])
                a_err = float(row["acc_std"].iloc[0])
                t_err = float(row["time_std"].iloc[0])
                # Manual sits at 0s; place it on the y-axis with a small floor
                t_plot = max(t, 1e-5) if m == "Manual" else t
                ax.errorbar(
                    t_plot, a, yerr=a_err, xerr=t_err if m != "Manual" else None,
                    fmt=METHOD_MARKERS[m], color=METHOD_COLORS[m],
                    ecolor=METHOD_COLORS[m], elinewidth=0.8, capsize=2.5,
                    markersize=7, markeredgecolor="black", markeredgewidth=0.5,
                    label=m,
                )

            ax.set_xscale("symlog", linthresh=1e-3)
            ax.set_title(f"{DATASET_LABEL[ds]} | {CLF_LABEL[clf]}", fontsize=9)
            if r == len(datasets) - 1:
                ax.set_xlabel("Wall-clock per seed (s, symlog)")
            if c == 0:
                ax.set_ylabel("Mean accuracy")
            ax.grid(True, alpha=0.25, linestyle=":")

    # shared legend
    handles, labels = axes[0, 0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="lower center", ncol=6, frameon=False,
               bbox_to_anchor=(0.5, -0.02))
    fig.tight_layout(rect=(0, 0.04, 1, 1))
    out = FIG / "time_accuracy_all.pdf"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    print("wrote", out)


# ----------------------------------------------------------------------
# Figure 2: box plots of per-seed accuracy, 2x3 facet
# ----------------------------------------------------------------------
def fig_box_plots_facet(raw: pd.DataFrame):
    datasets    = ["BC", "HD", "PD", "ILPD"]
    classifiers = ["RF", "SVM", "LR"]

    fig, axes = plt.subplots(4, 3, figsize=(9.2, 11.2), sharey=False)

    for r, ds in enumerate(datasets):
        for c, clf in enumerate(classifiers):
            ax = axes[r, c]
            sub = raw[(raw["dataset"] == ds) & (raw["classifier"] == clf)]
            data   = [sub[sub["method"] == m]["accuracy"].values for m in METHOD_ORDER]
            colors = [METHOD_COLORS[m] for m in METHOD_ORDER]

            bp = ax.boxplot(
                data, labels=METHOD_ORDER, patch_artist=True, widths=0.6,
                medianprops=dict(color="black", linewidth=1.4),
                flierprops=dict(marker="o", markersize=3, markerfacecolor="none",
                                markeredgecolor="black", alpha=0.6),
            )
            for patch, col in zip(bp["boxes"], colors):
                patch.set_facecolor(col)
                patch.set_alpha(0.55)
                patch.set_edgecolor("black")

            # overlay individual points
            for i, d in enumerate(data):
                jitter = np.random.default_rng(0).normal(0, 0.06, size=len(d))
                ax.scatter(np.full_like(d, i + 1) + jitter, d,
                           color="black", s=6, alpha=0.45, zorder=3)

            ax.set_title(f"{DATASET_LABEL[ds]} | {CLF_LABEL[clf]}", fontsize=9)
            if c == 0:
                ax.set_ylabel("Accuracy (per seed)")
            ax.grid(True, axis="y", alpha=0.25, linestyle=":")
            ax.tick_params(axis="x", rotation=20)

    fig.tight_layout()
    out = FIG / "box_plots_all.pdf"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    print("wrote", out)


# ----------------------------------------------------------------------
# Figure 3: ILPD/SVM hero figure -- Manual degeneracy vs HPO recovery
# ----------------------------------------------------------------------
def fig_ilpd_svm_story(raw: pd.DataFrame):
    sub = raw[(raw["dataset"] == "ILPD") & (raw["classifier"] == "SVM")].copy()

    fig, axes = plt.subplots(1, 2, figsize=(9.2, 3.8))

    # panel (a): accuracy vs macro-F1
    ax = axes[0]
    for m in METHOD_ORDER:
        d = sub[sub["method"] == m]
        ax.scatter(d["f1_macro"], d["accuracy"],
                   color=METHOD_COLORS[m], marker=METHOD_MARKERS[m],
                   s=55, alpha=0.85, edgecolor="black", linewidth=0.5, label=m)
    ax.axvline(0.5, linestyle="--", color="grey", linewidth=0.9, alpha=0.8)
    ax.text(0.502, ax.get_ylim()[0] + 0.003, "degenerate\nregime",
            fontsize=8, color="grey", alpha=0.9)
    ax.set_xlabel(r"Macro-$F_1$")
    ax.set_ylabel("Accuracy")
    ax.set_title("(a) ILPD / SVM: accuracy vs macro-$F_1$ per seed", fontsize=9.5)
    ax.grid(True, alpha=0.25, linestyle=":")

    # panel (b): FNR vs macro-F1  (fnr stored as fraction 0-1 in raw CSV; show as %)
    ax = axes[1]
    for m in METHOD_ORDER:
        d = sub[sub["method"] == m]
        ax.scatter(d["f1_macro"], 100.0 * d["fnr"],
                   color=METHOD_COLORS[m], marker=METHOD_MARKERS[m],
                   s=55, alpha=0.85, edgecolor="black", linewidth=0.5, label=m)
    ax.set_xlabel(r"Macro-$F_1$")
    ax.set_ylabel("False-negative rate (%)")
    ax.set_title(r"(b) ILPD / SVM: FNR vs macro-$F_1$ per seed", fontsize=9.5)
    ax.grid(True, alpha=0.25, linestyle=":")

    handles, labels = axes[0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="lower center", ncol=6, frameon=False,
               bbox_to_anchor=(0.5, -0.04))
    fig.tight_layout(rect=(0, 0.05, 1, 1))
    out = FIG / "ilpd_svm_story.pdf"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    print("wrote", out)


# ----------------------------------------------------------------------
# Figure 4: single-panel summary scatter across all 12 cells
# ----------------------------------------------------------------------
def fig_summary_scatter(summ: pd.DataFrame):
    # per-method: average accuracy loss vs Manual baseline and average time.
    rows = []
    for (ds, clf), grp in summ.groupby(["dataset", "classifier"]):
        manual_acc = float(grp.loc[grp["method"] == "Manual", "acc_mean"].iloc[0])
        for _, r in grp.iterrows():
            rows.append({
                "dataset": ds, "classifier": clf, "method": r["method"],
                "delta_acc": float(r["acc_mean"]) - manual_acc,
                "time_mean": max(float(r["time_mean"]), 1e-5),
            })
    df = pd.DataFrame(rows)
    df["method"] = pd.Categorical(df["method"], categories=METHOD_ORDER, ordered=True)

    # average across the 6 (dataset, classifier) cells
    avg = (df.groupby("method", observed=True)
             .agg(delta_acc_mean=("delta_acc", "mean"),
                  delta_acc_std =("delta_acc", "std"),
                  time_mean     =("time_mean", "mean"),
                  time_std      =("time_mean", "std"))
             .reindex(METHOD_ORDER))

    fig, ax = plt.subplots(figsize=(6.8, 4.2))
    manual_x = 1e-3  # fixed anchor for Manual on log axis
    for m in METHOD_ORDER:
        t = avg.loc[m, "time_mean"]
        d = avg.loc[m, "delta_acc_mean"]
        d_err = avg.loc[m, "delta_acc_std"]
        if m == "Manual":
            t_plot, xerr = manual_x, None
        else:
            t_plot = t
            # clip xerr so it does not reach zero/negative on log axis
            xerr = np.array([[min(avg.loc[m, "time_std"], 0.9 * t)],
                             [avg.loc[m, "time_std"]]])
        ax.errorbar(t_plot, d, yerr=d_err, xerr=xerr,
                    fmt=METHOD_MARKERS[m], color=METHOD_COLORS[m],
                    ecolor=METHOD_COLORS[m], elinewidth=0.9, capsize=3,
                    markersize=10, markeredgecolor="black", markeredgewidth=0.6,
                    label=m)
        ax.annotate(m, (t_plot, d),
                    xytext=(8, 5), textcoords="offset points",
                    fontsize=8.5, color=METHOD_COLORS[m])

    ax.axhline(0.0, color="grey", linestyle="--", linewidth=0.9, alpha=0.8)
    ax.set_xscale("log")
    ax.set_xlim(left=manual_x / 2)
    # label Manual's anchor as "~0"
    ax.annotate("(Manual anchored at ~0 s)",
                xy=(manual_x, ax.get_ylim()[1] * 0.9),
                fontsize=7.5, color="#666", ha="left")
    ax.set_xlabel("Wall-clock per seed (s, log) — averaged over twelve (dataset, classifier) cells")
    ax.set_ylabel(r"$\Delta$ accuracy vs Manual baseline")
    ax.set_title(r"Cross-cell HPO summary: accuracy gain vs compute cost")
    ax.grid(True, alpha=0.25, linestyle=":")
    ax.legend(frameon=False, loc="lower right", ncol=3)
    fig.tight_layout()
    out = FIG / "summary_scatter.pdf"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    print("wrote", out)


# ----------------------------------------------------------------------
# Figure 5: meta_heuristic_analysis.pdf (slot filler for fig:meta_analysis)
#   real panels: (c) box plot for PD/RF, (d) time-vs-accuracy for PD/RF
#   placeholder panels: (a) convergence, (b) diversity -- see supp. Colab
# ----------------------------------------------------------------------
def fig_meta_heuristic_analysis(raw: pd.DataFrame, summ: pd.DataFrame):
    ds, clf = "PD", "RF"
    sub_raw  = raw[(raw["dataset"] == ds) & (raw["classifier"] == clf)]
    sub_summ = summ[(summ["dataset"] == ds) & (summ["classifier"] == clf)]

    fig, axes = plt.subplots(2, 2, figsize=(9.2, 6.8))

    # ---- panel (a): placeholder for convergence curves
    ax = axes[0, 0]
    ax.text(0.5, 0.5,
            "(a) Convergence curves\n(best-so-far vs iteration)\n\n"
            "Placeholder — regenerated by\nsupplementary Colab notebook\n"
            "(instruments GWO / PSO with\nper-iteration logging)",
            ha="center", va="center", fontsize=9, color="#444",
            bbox=dict(boxstyle="round,pad=0.6", facecolor="#f5f5f5",
                      edgecolor="#bbb", linewidth=0.8))
    ax.set_xlim(0, 1); ax.set_ylim(0, 1); ax.set_xticks([]); ax.set_yticks([])
    ax.set_title("(a) Convergence (placeholder)", fontsize=10)
    for s in ax.spines.values():
        s.set_visible(True); s.set_color("#bbb")

    # ---- panel (b): placeholder for population diversity
    ax = axes[0, 1]
    ax.text(0.5, 0.5,
            "(b) Population diversity\n(mean pair-wise distance vs iteration)\n\n"
            "Placeholder — regenerated by\nsupplementary Colab notebook",
            ha="center", va="center", fontsize=9, color="#444",
            bbox=dict(boxstyle="round,pad=0.6", facecolor="#f5f5f5",
                      edgecolor="#bbb", linewidth=0.8))
    ax.set_xlim(0, 1); ax.set_ylim(0, 1); ax.set_xticks([]); ax.set_yticks([])
    ax.set_title("(b) Diversity (placeholder)", fontsize=10)
    for s in ax.spines.values():
        s.set_visible(True); s.set_color("#bbb")

    # ---- panel (c): box plot of accuracy (PD/RF)
    ax = axes[1, 0]
    data = [sub_raw[sub_raw["method"] == m]["accuracy"].values for m in METHOD_ORDER]
    bp = ax.boxplot(data, labels=METHOD_ORDER, patch_artist=True, widths=0.6,
                    medianprops=dict(color="black", linewidth=1.4))
    for patch, m in zip(bp["boxes"], METHOD_ORDER):
        patch.set_facecolor(METHOD_COLORS[m]); patch.set_alpha(0.55)
        patch.set_edgecolor("black")
    for i, d in enumerate(data):
        j = np.random.default_rng(0).normal(0, 0.06, size=len(d))
        ax.scatter(np.full_like(d, i + 1) + j, d, color="black", s=8, alpha=0.5, zorder=3)
    ax.set_title("(c) Accuracy distribution — Pima / RF", fontsize=10)
    ax.set_ylabel("Accuracy")
    ax.grid(True, axis="y", alpha=0.25, linestyle=":")
    ax.tick_params(axis="x", rotation=15)

    # ---- panel (d): time vs accuracy (PD/RF)
    ax = axes[1, 1]
    for m in METHOD_ORDER:
        row = sub_summ[sub_summ["method"] == m]
        if row.empty:
            continue
        t = max(float(row["time_mean"].iloc[0]), 1e-5)
        a = float(row["acc_mean"].iloc[0])
        a_err = float(row["acc_std"].iloc[0])
        t_err = float(row["time_std"].iloc[0])
        ax.errorbar(t, a, yerr=a_err, xerr=t_err if m != "Manual" else None,
                    fmt=METHOD_MARKERS[m], color=METHOD_COLORS[m],
                    ecolor=METHOD_COLORS[m], elinewidth=0.9, capsize=3,
                    markersize=9, markeredgecolor="black", markeredgewidth=0.5,
                    label=m)
        ax.annotate(m, (t, a), xytext=(7, 4), textcoords="offset points",
                    fontsize=8.5, color=METHOD_COLORS[m])
    ax.set_xscale("symlog", linthresh=1e-3)
    ax.set_title("(d) Time vs accuracy — Pima / RF", fontsize=10)
    ax.set_xlabel("Wall-clock per seed (s, symlog)")
    ax.set_ylabel("Mean accuracy")
    ax.grid(True, alpha=0.25, linestyle=":")

    fig.tight_layout()
    out = FIG / "meta_heuristic_pd_rf.pdf"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    print("wrote", out)


def main():
    raw, summ = load_data()
    fig_time_accuracy_facet(raw, summ)
    fig_box_plots_facet(raw)
    fig_ilpd_svm_story(raw)
    fig_summary_scatter(summ)
    fig_meta_heuristic_analysis(raw, summ)
    print("\nAll figures written to", FIG)


if __name__ == "__main__":
    main()
